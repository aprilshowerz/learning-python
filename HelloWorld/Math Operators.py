# how operators work.

# plus sign does addition.
print(10 + 3)

# minus sign does subtraction.
print(10 - 3)

# asterisk does multiplication. (10 to the power of 3)
print(10 * 3)

# 2 asterisks does exponents.
print(10 ** 3)

# one slash returns a float (decimal number)
print(10 / 3)

# 2 slashes returns an integer (whole number)
print(10 // 3)

# modulus returns remainder of division.
print(10 % 3)


x = 10
print(x)

# augmented assignment operator addition; same as x = x + 3
x += 3
print(x)

# augmented assignment operator subtraction; same as x = x - 3
x -= 3
print(x)

# augmented assignment operator multiplication; same as x = x * 3
x *= 3
print(x)

# augmented assignment operator division; same as x = x / 3
x /= 3
print(x)

