# comparison operators

# boolean expressions greater than
x = 3 > 2
print(x)

# greater than or equal to
x = 3 >= 2

# less than or equal to
x = 3 <= 2

# equality operator. Is 3 equal to 2? False
x = 3 == 2

# inequality operator. Is 3 NOT equal to 2? True
x = 3 != 2

# price is greater than 10 and less than 30 often used in if statements. should return true.
price = 25
print(price > 10 and price < 30)

# price is greater than 10 or less than 30. often used in if statements. should return true.
print(price > 10 or price < 30)

# reverses boolean answer. returns the opposite.
print(not price > 10)


